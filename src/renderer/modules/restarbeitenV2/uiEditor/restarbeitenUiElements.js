const RESTARBEITEN_UI_EDITOR_SCOPE = "restarbeiten.screen";

const LOCKED_LAYOUT_OPS = Object.freeze([
  "show",
  "hide",
  "move",
  "resize",
  "reorder",
  "rename",
  "changeWidth",
  "pin",
  "unpin",
  "reset",
  "applyPreset",
]);

function element({
  id,
  name,
  type,
  role,
  parentId,
  order,
  editable = false,
  allowedOps = ["inspect"],
  lockedOps = LOCKED_LAYOUT_OPS,
  fieldKind,
  actionKind,
  componentKind,
}) {
  return Object.freeze({
    id,
    name,
    type,
    role,
    parentId,
    order,
    visible: true,
    editable,
    allowedOps: Object.freeze([...allowedOps]),
    lockedOps: Object.freeze([...lockedOps]),
    ...(fieldKind ? { fieldKind } : {}),
    ...(actionKind ? { actionKind } : {}),
    ...(componentKind ? { componentKind } : {}),
  });
}

