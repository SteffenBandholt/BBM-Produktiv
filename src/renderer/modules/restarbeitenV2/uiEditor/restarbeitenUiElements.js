function cloneElement(entry) {
  return {
    ...entry,
    allowedOps: [...entry.allowedOps],
    lockedOps: [...entry.lockedOps],
  };
}

export function getRestarbeitenUiEditorElements() {
  return RESTARBEITEN_UI_EDITOR_ELEMENTS.map(cloneElement);
}

